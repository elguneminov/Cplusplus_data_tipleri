#include <iostream>
using namespace std;

int main()
{
    cout<<"char olcusu: "<<sizeof(char)<<" bayt"<<endl;
    cout<<"int olcusu: "<<sizeof(int)<<" bayt"<<endl;
    cout<<"short int olcusu: "<<sizeof(short int)<<" bayt"<<endl;
    cout<<"long int olcusu: "<<sizeof(long int)<<" bayt"<<endl;
    cout<<"signed long int olcusu: "<<sizeof(signed long int)<<" bayt"<<endl;
    cout<<"float olcusu: "<<sizeof(float)<<" bayt"<<endl;
    cout<<"double olcusu: "<<sizeof(double)<<" bayt"<<endl;
    cout<<"wchar_t olcusu: "<<sizeof(wchar_t)<<" bayt"<<endl;

    return 0;
}
